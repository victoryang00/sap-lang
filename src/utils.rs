pub fn is_ws(s: char) -> bool {
    match s {
        '\u{0009}' => true,
        '\u{000A}' => true,
        '\u{000B}' => true,
        '\u{000C}' => true,
        '\u{000D}' => true,
        '\u{0020}' => true,
        '\u{0085}' => true,
        '\u{200E}' => true,
        '\u{200F}' => true,
        '\u{2028}' => true,
        '\u{2029}' => true,
        _ => false,
    }
}

pub fn ws(s: &str) -> IResult<&str, &str> {
    // '\u{0009}' (horizontal tab, '\t')
    // '\u{000A}' (line feed, '\n')
    // '\u{000B}' (vertical tab)
    // '\u{000C}' (form feed)
    // '\u{000D}' (carriage return, '\r')
    // '\u{0020}' (space, ' ')
    // '\u{0085}' (next line)
    // '\u{200E}' (left-to-right mark)
    // '\u{200F}' (right-to-left mark)
    // '\u{2028}' (line separator)
    // '\u{2029}' (paragraph separator)
    take_till1(|c| !is_ws(c))(s)
}

pub fn ws_single_line(s: &str) -> IResult<&str, &str> {
    // '\u{0009}' (horizontal tab, '\t')
    // '\u{000A}' (line feed, '\n')
    // '\u{000B}' (vertical tab)
    // '\u{000C}' (form feed)
    // '\u{000D}' (carriage return, '\r')
    // '\u{0020}' (space, ' ')
    // '\u{0085}' (next line)
    // '\u{200E}' (left-to-right mark)
    // '\u{200F}' (right-to-left mark)
    // '\u{2028}' (line separator)
    // '\u{2029}' (paragraph separator)
    take_till1(|c| c == '\n' || c == '\r' || c == '\u{0085}' || !is_ws(c))(s)
}

pub fn list0<'a, O>(
    s: &'static str,
    e: &'static str,
    sep: &'static str,
    inner: impl Fn(&'a str) -> IResult<&'a str, O>,
) -> impl FnMut(&'a str) -> IResult<&'a str, Vec<O>> {
    delimited(
        pair(tag(s), opt(ws)),
        separated_list0(pair(tag(sep), opt(ws)), inner),
        tuple((opt(ws), opt(tag(sep)), opt(ws), tag(e))),
    )
}

pub fn list1<'a, O>(
    s: &'static str,
    e: &'static str,
    sep: &'static str,
    inner: impl Fn(&'a str) -> IResult<&'a str, O>,
) -> impl FnMut(&'a str) -> IResult<&'a str, Vec<O>> {
    delimited(
        pair(tag(s), opt(ws)),
        separated_list1(pair(tag(sep), opt(ws)), inner),
        tuple((opt(ws), opt(tag(sep)), opt(ws), tag(e))),
    )
}

